<h3>How is it used?</h3>
<p>First, install it, then set relevant permissions.</p>
<h4>Creating a form</h4>
<p>In the CMS Admin Menu, suitably authorised users will see a menu
item called "Power Forms". Click on that. On the displayed page (at the
bottom of the list of forms), there are objects which can be activated
to add a new form.</p>
<h4>Displaying a form</h4>
<p>The tag used to display each form is shown in the module's admin page. Each tag is like
<code>{PowerForms form='sample_form'}</code>.</p>
<p>Placing the relevant tag into the content of a website page or template will cause that
form to be displayed. Be careful when pasting the tag into a page's content if you use a
WYSIWYG editor such as TinyMCE. These editors may stealthily change quote marks (") into
HTML entities (&amp;quot;), and the forms will not show up. Use single quotes (') or edit
the HTML directly.</p>
